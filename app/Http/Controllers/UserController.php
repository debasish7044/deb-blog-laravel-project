     public function makeAdmin( $id)
    {
       $users = User::where('id',$id)->get();
       
        foreach ($users as $key => $user) {
         $user->role = 'admin';
         $user->save();
       }

       return redirect('/users')->with('success', $user->name . ' is admin now');
    }
     public function makeWriter( $id)
